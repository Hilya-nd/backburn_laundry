    private void loadDataWilayah() {
        try {
            List<WilayahSistem> list = WilayahSistem.loadData();

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            for (WilayahSistem w : list) {
                model.addRow(new Object[]{
                    w.getIdWilayah(),
                    w.getNamaWilayah(),
                    w.getJarak()
                });
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data wilayah!");
        }
    }

     private void tambahWilayah() {
        try {
            String idStr = JOptionPane.showInputDialog("Masukkan ID Wilayah:");
            String nama = JOptionPane.showInputDialog("Masukkan Nama Wilayah:");
            String jarakStr = JOptionPane.showInputDialog("Masukkan Jarak:");

            if (idStr == null || nama == null || jarakStr == null ||
                idStr.isEmpty() || nama.isEmpty() || jarakStr.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Input tidak boleh kosong!");
                return;
            }

            int id = Integer.parseInt(idStr);
            double jarak = Double.parseDouble(jarakStr);

            WilayahSistem.tambah(id, nama, jarak);

            JOptionPane.showMessageDialog(this, "Wilayah berhasil ditambahkan!");
            loadDataWilayah();

        } catch (NumberFormatException nfe) {
            JOptionPane.showMessageDialog(this, "ID dan jarak harus berupa angka!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal menambah wilayah!");
        }
    }
    
    private void hapusWilayah() {
        int row = jTable1.getSelectedRow();

        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data terlebih dahulu!");
            return;
        }

        int id = Integer.parseInt(jTable1.getValueAt(row, 0).toString());

        int confirm = JOptionPane.showConfirmDialog(
                this,
                "Hapus wilayah dengan ID: " + id + "?",
                "Konfirmasi",
                JOptionPane.YES_NO_OPTION
        );

        if (confirm == JOptionPane.YES_OPTION) {
            try {
                WilayahSistem.hapus(id);
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
                loadDataWilayah();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Gagal menghapus data!");
            }
        }
    }


