     private void loadTable() {
        try {
            List<KategoriSistem> list = KategoriSistem.loadKategori();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            for (KategoriSistem k : list) {
                model.addRow(new Object[]{
                        k.getId(),
                        k.getNama(),
                        k.getHargaPerKg()
                });
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data: " + e.getMessage());
        }
    }
     
    private void tambahKategoriPopup() {
        String nama = JOptionPane.showInputDialog(this, "Masukkan nama kategori:");

        if (nama == null || nama.trim().isEmpty()) return;

        String hargaStr = JOptionPane.showInputDialog(this, "Masukkan harga per kg:");

        try {
            double harga = Double.parseDouble(hargaStr);

            KategoriSistem.tambahKategori(nama, harga);

            JOptionPane.showMessageDialog(this, "Kategori berhasil ditambahkan!");
            loadTable();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Harga harus angka!");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal menambahkan: " + e.getMessage());
        }
    }

